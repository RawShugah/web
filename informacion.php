        <h2>La Obesidad y sus Riesgos</h2>
        <p>La obesidad es una condición médica compleja que involucra un exceso de grasa corporal. Es un factor de riesgo importante para enfermedades como diabetes tipo 2, hipertensión, enfermedades cardíacas, y más. Si no se controla, puede afectar gravemente la calidad de vida.</p>

        <h2>Cómo el Deporte Puede Ayudar</h2>
        <p>La actividad física regular es fundamental para mantener un peso saludable. El deporte no solo ayuda a quemar calorías, sino que también mejora la salud mental y previene diversas enfermedades. Aquí te dejamos algunos consejos útiles:</p>

        <h3>Consejos para Incorporar el Deporte en tu Vida:</h3>
        <ul>
            <li>Haz ejercicio al menos 30 minutos al día.</li>
            <li>Encuentra una actividad que disfrutes: caminar, correr, nadar, bailar, etc.</li>
            <li>Consigue un compañero de entrenamiento para mantener la motivación.</li>
            <li>Establece metas realistas y progresa poco a poco.</li>
        </ul>

